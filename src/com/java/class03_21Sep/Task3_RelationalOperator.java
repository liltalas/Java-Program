package com.java.class03_21Sep;

public class Task3_RelationalOperator {
    public static void main(String[] args) {

        int a = 1, b = -1; // declaring variables and assigning values

        //display output as a true or false
        System.out.println(a > b); // true
        System.out.println(a < b); // false
        System.out.println(a >= b); // true
        System.out.println(a <= b); // false
        System.out.println(a != b); // true
        System.out.println(a == b); // false
    }
}
