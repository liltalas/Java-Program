package com.java.class04_26Sep;

public class RevisionOfModulo {
    public static void main(String []args){

        int num1 = 2, num2 = 3;

        // whenever the second number is bigger than the first number, the first num will always be the remainder
        // we can divide it, but we can't do it in java from integer perspective
        System.out.println(num1 % num2);
    }
}
